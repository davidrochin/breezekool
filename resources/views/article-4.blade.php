<p>Uno de los grandes problemas de las cocinas industriales es conseguir un ambiente de trabajo confortable. Los grandes
    electrodomésticos (frigoríficos, congeladores, lavaplatos…) generan calor en su funcionamiento normal, que se suma
    al de los fogones y hornos. La climatización se convierte en un reto que muchos restaurantes dan por inalcanzable o
    simplemente se resignan a vencer pagando cantidades exorbitantes. Eso es porque no usan climatizadores evaporativos.
</p>

<p>En una cocina, los extractores “chupan” el aire haciendo inútil intentar enfriar el ambiente con aparatos de aire
    acondicionado. Es evidente: en cuanto esos aparatos producen aire frío, este se va por los extractores junto con los
    humos de la cocina. Esto obliga a que el aire acondicionado esté funcionando forzado y sin llegar nunca al punto
    óptimo de refrigeración. Es un consumo eléctrico desorbitado y la factura crece año tras año.</p>

<p>Los extractores sacan al aire interior, forzando al aire exterior limpio a entrar en la cocina. Esto es una
    refrigeración en sí misma. Pero ¿qué ocurre cuando el aire exterior está tan caliente o más que el interior? Los
    extractores simplemente traen nuevo aire caliente, algo muy común en verano. Sin embargo, podemos aprovechar este
    flujo de aire en nuestro provecho con los climatizadores evaporativos.</p>

<p>Un climatizador evaporativo refrigera aprovechando el fenómeno físico del “proceso adiabático”. Para entenderlo
    debemos hablar del concepto de “calor latente”. Cuando un cuerpo pasa de sólido a líquido o de líquido a gaseoso, es
    porque se le ha aplicado energía calorífica que lo provoca. Esa energía es el calor latente. Lo curioso es que
    cuando se produce el cambio, la sustancia no cambia de temperatura. Eso es el proceso adiabático. Cuando una
    sustancia como el agua líquida recibe energía calorífica del aire que la rodea, esta se usa para transformarla en
    vapor. La energía calorífica del aire se ha invertido en el calor latente del agua (produciendo evaporación), no en
    subir su temperatura.</p>

<p>Si se trata de una corriente de aire caliente el resultado es su enfriamiento y el aumento de la humedad del ambiente
    por el vapor generado. ¿Cómo podemos aprovechar los extractores de la cocina? Estos evacúan el aire caliente y
    provocan una corriente de aire externo que los climatizadores evaporativos aprovechan interponiendo un filtro
    húmedo. Así, se produce el enfriamiento adiabático del aire entrante canalizado y se regula la humedad de la cocina,
    en un ciclo que refresca y limpia el ambiente continuamente.</p>

<p>Sin gases refrigerantes ni instalaciones complicadas, es un mecanismo sencillo y natural de coste eléctrico mínimo
    (una bomba de agua y un ventilador): se estima que climatizar una cocina de 100 m2 consumiría 1,2 kWh, que es lo que
    consume el aire acondicionado para 20 m2. Con un ahorro así es una opción que se convertirá en prioritaria para
    cualquier restaurante u hotel.</p>

