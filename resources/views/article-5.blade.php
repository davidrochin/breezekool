<p>Es innegable que resulta muy difícil luchar contra el calor acumulado en las naves industriales, especialmente en las
    granjas donde los propios animales generan calor. Este hecho se ve agravado en las granjas, donde, como sucede en
    los talleres mecánicos, las altas temperaturas van acompañadas de gases y olores que dificultan el trabajo. Para
    remediar todos estos problemas, los climatizadores evaporativos son nuestra solución ideal.</p>

<h2>Los animales y su temperatura corporal.</h2>

<p>En general, los animales, al contrario que en el caso de los humanos, no son sensibles a la humedad y tampoco sudan.
    Hay casos excepcionales, como sucede con los equinos, pero la mayoría de animales debe disipar el calor por métodos
    de convección o radiación a través de piel, pelo o plumas, ya que solo una pequeña parte de su calor corporal se
    evapora en forma de humedad (por lo general, nasal u oral; pero también bronquial, cuando el animal jadea para
    refrescarse). Pero, al disipar el calor a su alrededor, este mecanismo de protección natural no hace más que
    aumentar la temperatura ambiente en un espacio cerrado y agravar, en realidad, el problema.</p>

<p>Todo esto hace que los animales sufran de forma particular las altas temperaturas. El ganado vacuno lo sobrelleva
    mejor, gracias a una superficie externa más amplia y algunas regiones internas evaporativas. Pero las aves de corral
    y el ganado porcino son más sensibles. De cualquier forma, pocos ejemplares sobreviven cuando la temperatura
    ambiental se aproxima o, incluso, sobrepasa la de su cuerpo. Ni siquiera deja de ser preocupante, si tomamos en
    consideración que casi todos los animales de granja tienen una temperatura corporal más alta que la de los humanos.
    Por ejemplo, en los pollos, se sitúa en los 41’7 ºC.</p>

<p>Pero mucho antes de esos grados, cuando la temperatura del aire pasa de unos 24 ºC, la productividad de los animales
    se ve disminuida. De esta forma, las gallinas jóvenes y el resto de aves de corral reducen su producción de huevos a
    partir de esa temperatura.</p>

<p>Así pues, parece claro que la temperatura del entorno, la cantidad de humedad en el aire y el movimiento de este se
    convierten en factores de una importancia capital para la salud y productividad de los animales de granja.</p>

<h2>La importancia de la climatización evaporativa.</h2>

<p>Si mantener las granjas refrigeradas tiene tal importancia y la humedad y ventilación afectan de tal manera a su
    funcionamiento, el siguiente paso lógico es plantear una solución para controlar todos estos factores. El problema
    llega al descubrir que la refrigeración tradicional es una medida costosa, que, por lo general, requiere de unos
    edificios especiales.</p>

<p>Además, las granjas deben ventilarse, con el fin de eliminar el aire viciado y las molestas concentraciones de
    dióxido carbónico y amonios, por lo que, en todo momento, debe existir una ventilación de, al menos, una cuarta
    parte del valor máximo para la instalación. Por otro lado, la temperatura y humedad no deben sobrepasar la zona de
    confort, en todas las etapas del proceso: sea el lugar de puesta, la zona de los criaderos o los corrales donde se
    almacenan los animales para ser sacrificados.</p>

<p>Estas exigencias nos dejan como única respuesta la climatización evaporativa, un sistema eficaz, rentable y
    sostenible, el cual funciona, por igual, en talleres, naves industrias o granjas. Gracias a los climatizadores
    evaporativos, el aire húmedo y viciado de la granja es sustituido por masas de aire más fresco y seco. De esta
    manera, logramos mejorar la salud de los animales y la productividad del negocio de una forma económica y ecológica,
    gracias a su probada eficiencia.</p>

