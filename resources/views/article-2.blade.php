</p>

<h3>Ventilación natural o forzada</h3>

<p><img class="size-full wp-image-1828 alignleft"
        src="{{ URL::to('/') }}/img/bio-article-2-3.png"
        alt="ventilación natural forzada en nave industrial" width="320" height="232"
        sizes="(max-width: 320px) 100vw, 320px" />Los climatizadores evaporativos recogen aire del exterior, lo
    humedecen, <strong>lo inyectan dentro y finalmente lo expulsan nuevamente al exterior</strong>.</p>
<p><strong>Generan una ligera sobrepresión</strong> dentro gracias a <strong>renovaciones continuas del aire</strong>
    que expulsar el calor al exterior.</p>
<p>El sistema permite <strong>expulsar el aire de forma natural</strong>, abriendo puertas y/o ventanas, o <strong>de
        forma forzada con extractores</strong>.</p>
<p>Así que, tanto si necesitas <strong>trabajar con las puertas abiertas</strong>, como si necesitas <strong>espacios
        completamente estancos</strong>, los climatizadores evaporativos son perfectos para ti.</p>

