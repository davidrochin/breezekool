<h3>¿Qué es un climatizador evaporativo?</h3>

<p><img class="size-full wp-image-1826 alignleft"
    src="{{ URL::to('/') }}/img/bio-article-2-2.png"
        alt="climatizador evaporativo corriente de aire nave industrial" width="320" height="232"
        sizes="(max-width: 320px) 100vw, 320px" />Los evaporativos son equipos de climatización industrial que utilizan
    <strong>aire húmedo para reducir la temperatura de grandes espacios, </strong>como por ejemplo naves industriales.
</p>
<p>Los evaporativos basan su funcionamiento en el <strong>proceso adiabático</strong>, que consiste en inyectar aire
    húmedo de forma constante para generar un <strong>equilibrio de sobrepresión que permita expulsar el calor al
        exterior</strong>.</p>
<p>Los climatizadores evaporativos son ideales para ventilar una nave industrial porque <strong>también permiten
        inyectar aire seco</strong>, y de esto modo <strong>substituyen a los ventiladores y extractores
        convencionales</strong>.</p>
